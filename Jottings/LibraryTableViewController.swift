    // MARK: - Segues
    
    override func prepare(for segue: UIStoryboardSegue, sender: AnyObject?) {
        if segue.identifier == "showDetail" {
            if let indexPath = self.tableView.indexPathForSelectedRow {
                let object = self.fetchedResultsController.object(at: indexPath)
                let controller = (segue.destination as! UINavigationController).topViewController as! JottingViewController
                controller.detailItem = object
                controller.navigationItem.leftBarButtonItem = self.splitViewController?.displayModeButtonItem
                controller.navigationItem.leftItemsSupplementBackButton = true
            }
        }
    }
    
    
    // MARK: - Table view data source
    
    override func numberOfSections(in tableView: UITableView) -> Int {
        // #warning Incomplete implementation, return the number of sections
        
        let result = self.fetchedResultsController.sections?.count ?? 0
        return result + 1
    }
    
    override func tableView(_ tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        // #warning Incomplete implementation, return the number of rows
        
        if section == 0 {
            return sortDescriptors.count
        } else {
            let sectionInfo = self.fetchedResultsController.sections![section - 1]
            return sectionInfo.numberOfObjects
        }
    }
    
    
    override func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        
        if (indexPath as NSIndexPath).section == 0 {
            let cell = tableView.dequeueReusableCell(withIdentifier: "libraryCell", for: indexPath)
            cell.textLabel?.text = sortDescriptors[indexPath.row]
            return cell
            
        } else {
            let cell = tableView.dequeueReusableCell(withIdentifier: "recentCell", for: indexPath)
            
            let modelIndexPath = IndexPath(row: indexPath.row, section: indexPath.section - 1)
            
            let jotting = self.fetchedResultsController.object(at: modelIndexPath)
       
            configureCell(cell, content: jotting)
            
            return cell
        }
    }
    
    func configureCell(_ cell: UITableViewCell, content: Jotting) {
        
        let latestVersion = content.latestVersion
        
        cell.textLabel?.text = latestVersion?.title
        cell.detailTextLabel?.text = String(latestVersion?.edited)
    }
 
    override func tableView(_ tableView: UITableView, canEditRowAt indexPath: IndexPath) -> Bool {
        // Return false if you do not want the specified item to be editable.
        
        if indexPath.section == 0 {
            return false
        }
        
        return true
    }
    
    override func tableView(_ tableView: UITableView, commit editingStyle: UITableViewCellEditingStyle, forRowAt indexPath: IndexPath) {
        if editingStyle == .delete {
            let context = self.fetchedResultsController.managedObjectContext
            context.delete(self.fetchedResultsController.object(at: indexPath))
            
            do {
                try context.save()
            } catch {
                // Replace this implementation with code to handle the error appropriately.
